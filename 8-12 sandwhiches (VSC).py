def make_sandwhich(*items):
    print("\nI'll make you a great sandwhich: ")
    for item in items:
        print(f" ....adding {item} to your sandwhich ")
    print(("Your sandwhich is ready! "))

make_sandwhich('roast beef','cheddar cheese','lettuce','honey dijon')
make_sandwhich('turkey','apple slices','hoeny mustard')
make_sandwhich('penut butter','strawberry jam')

